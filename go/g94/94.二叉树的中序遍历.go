/**
 * Definition for a binary tree node.
 * type TreeNode struct {
 *     Val int
 *     Left *TreeNode
 *     Right *TreeNode
 * }
 */
func inorderTraversal(root *TreeNode) []int {
	r := []int{}
	var f func(*TreeNode)
	f = func(tn *TreeNode) {
		if tn == nil {
			return
		}
		f(tn.Left)
		r = append(r, tn.Val)
		f(tn.Right)
	}
	f(root)
	return r
}

// @lc code=end

type TreeNode struct {
	Val   int
	Left  *TreeNode
	Right *TreeNode
}
